import React from 'react'

interface SectionHeaderBadgeProps {
  children: React.ReactNode
  icon?: React.ReactNode
  variant?: 'primary' | 'accent' | 'secondary' | 'hero'
  className?: string
}

/**
 * SectionHeaderBadge - Consistent badge component for section headers
 *
 * @param children - Badge text content
 * @param icon - Optional icon element (SVG)
 * @param variant - Color variant (primary, accent, secondary)
 * @param className - Additional Tailwind classes
 *
 * @example
 * <SectionHeaderBadge
 *   icon={<svg>...</svg>}
 *   variant="accent"
 * >
 *   Notre Strat√©gie
 * </SectionHeaderBadge>
 */
export function SectionHeaderBadge({
  children,
  icon,
  variant = 'accent',
  className = ''
}: SectionHeaderBadgeProps) {
  const variantStyles = {
    primary: 'bg-primary-500/10 dark:bg-primary-500/20 text-primary-700 dark:text-primary-400',
    accent: 'bg-accent-500/10 dark:bg-accent-500/20 text-primary-700 dark:text-accent-500',
    secondary: 'bg-gray-500/10 dark:bg-gray-500/20 text-gray-700 dark:text-gray-300',
    hero: 'bg-accent-500/30 dark:bg-accent-500/40 backdrop-blur-sm text-accent-500 dark:text-accent-400 shadow-lg shadow-accent-500/20 border border-accent-500/30'
  }

  return (
    <div className={`inline-block ${variantStyles[variant]} px-4 py-2 rounded-full mb-4 ${className}`}>
      <span className="flex items-center gap-2 justify-center font-semibold">
        {icon && <span className="w-4 h-4 flex-shrink-0">{icon}</span>}
        {children}
      </span>
    </div>
  )
}

